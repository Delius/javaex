

	class GraphNode {
	    private int UDBNumber;
	    private int second;
	    private int third;
	    private int fourth;

	    public GraphNode(int UDBNumber, int second, int third, int fourth) {                
	            this.UDBNumber = UDBNumber;
	            this.second = second;
	            this.third = third;
	            this.fourth = fourth;
	    }

	    public int getUDBNumber() {
	            return UDBNumber;
	    }

	    public void setUDBNumber(int UDBNumber) {
	            this.UDBNumber = UDBNumber;
	    }

	    public int getsecond() {
	            return second;
	    }

	    public void setsecond(int second) {
	            this.second = second;
	    }

	    public int getthird() {
	            return third;
	    }

	    public void setthird(int third) {
	        this.third = third;
	    }


}
